        function initializeVideoPlayer() {
            const video = document.getElementById('player');
            const source = video.querySelector('source').src;

            if (Hls.isSupported()) {
                const hls = new Hls();
                hls.loadSource(source);
                hls.attachMedia(video);
                hls.on(Hls.Events.MANIFEST_PARSED, () => {
                    video.play();
                });
            } else if (video.canPlayType('application/vnd.apple.mpegurl')) {
                video.src = source;
                video.addEventListener('loadedmetadata', () => {
                    video.play();
                });
            } else {
                console.error("HLS não é suportado neste navegador.");
            }
            const player = new Plyr(video, {
                captions: {
                    active: true
                }
            });
        }

        function displayCurrentSBTProgram() {
            const scheduleData = [
                { time: '06:00', program: 'Primeiro Impacto 1ª Edição' },
                { time: '07:15', program: 'Primeiro Impacto 2ª Edição' },
                { time: '09:30', program: 'Chega Mais' },
                { time: '13:30', program: 'Tá na Hora' },
                { time: '17:30', program: 'SBT Brasil' },
                { time: '19:45', program: 'Novela Carrossel' },
                { time: '20:30', program: 'Novela Teresa' },
                { time: '21:15', program: 'A Infância de Romeu e Julieta' },
                { time: '22:00', program: 'Programa do Ratinho' },
                { time: '23:15', program: 'The Noite com Danilo Gentili' },
                { time: '00:30', program: 'Operação Mesquita' },
                { time: '01:00', program: 'SBT News na TV' },
                { time: '05:00', program: 'Jornal da Manhã' }
            ];

            const now = new Date();
            const currentHour = now.getHours();
            const currentMinute = now.getMinutes();
            const currentTimeInMinutes = currentHour * 60 + currentMinute;

            let currentProgram = "No Ar";
            let foundProgram = false;

            for (let i = 0; i < scheduleData.length; i++) {
                const programTimeParts = scheduleData[i].time.split(':');
                const programHour = parseInt(programTimeParts[0], 10);
                const programMinute = parseInt(programTimeParts[1], 10);
                const programTimeInMinutes = programHour * 60 + programMinute;

                let nextProgramTimeInMinutes;
                if (i + 1 < scheduleData.length) {
                    const nextProgramTimeParts = scheduleData[i + 1].time.split(':');
                    const nextProgramHour = parseInt(nextProgramTimeParts[0], 10);
                    const nextProgramMinute = parseInt(nextProgramTimeParts[1], 10);
                    nextProgramTimeInMinutes = nextProgramHour * 60 + nextProgramMinute;
                } else {
                    nextProgramTimeInMinutes = 24 * 60; // Até meia-noite para o último programa
                }

                if (currentTimeInMinutes >= programTimeInMinutes && currentTimeInMinutes <
                    nextProgramTimeInMinutes) {
                    currentProgram = scheduleData[i].program;
                    foundProgram = true;
                    break;
                }
            }

            const currentProgramDiv = document.getElementById('current-program-sbt');
            currentProgramDiv.textContent = currentProgram + ' '; // Adiciona um espaço após o nome do programa
            const liveSpan = document.createElement('span'); // Cria um span para "AO VIVO"
            liveSpan.textContent = 'AO VIVO';
            currentProgramDiv.appendChild(liveSpan); // Adiciona o span dentro da div
        }
